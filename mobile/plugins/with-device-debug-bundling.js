const fs = require("fs");
const path = require("path");
const { withDangerousMod, withXcodeProject } = require("@expo/config-plugins");

const USER_SCRIPT_SANDBOXING_YES = "ENABLE_USER_SCRIPT_SANDBOXING = YES;";
const USER_SCRIPT_SANDBOXING_NO = "ENABLE_USER_SCRIPT_SANDBOXING = NO;";
const PODFILE_PLATFORM_LINE =
  "platform :ios, podfile_properties['ios.deploymentTarget'] || '15.1'\n";
const PODFILE_INHIBIT_WARNINGS_LINE = "inhibit_all_warnings!\n";
const SCHEME_LAUNCH_DEBUG =
  '<LaunchAction\n      buildConfiguration = "Debug"';
const SCHEME_LAUNCH_RELEASE =
  '<LaunchAction\n      buildConfiguration = "Release"';
const BUNDLE_PHASE_NAME = "Bundle React Native code and images";
const BUNDLE_SCRIPT = `#!/bin/sh
set -e

# Load base env (generated by Expo) if present
if [ -f "$PODS_ROOT/../.xcode.env" ]; then
  # shellcheck disable=SC1090
  . "$PODS_ROOT/../.xcode.env"
fi

# Load local env overrides (developer machine) if present
if [ -f "$PODS_ROOT/../.xcode.env.local" ]; then
  # shellcheck disable=SC1090
  . "$PODS_ROOT/../.xcode.env.local"
fi

# The project root by default is one level up from the ios directory
export PROJECT_ROOT="$PROJECT_DIR/.."

# --- Bundling behavior -------------------------------------------------------
# Debug + Simulator: skip bundling (Metro/dev client workflow)
if [ "\${CONFIGURATION#*Debug}" != "$CONFIGURATION" ] && echo "$PLATFORM_NAME" | grep -q "simulator"; then
  export SKIP_BUNDLING=1
fi

# Debug + Device (iphoneos): we are embedded, so DEVTOOLS websocket must be OFF
# (Even if env files try to turn it on later)
if [ "\${CONFIGURATION#*Debug}" != "$CONFIGURATION" ] && ! echo "$PLATFORM_NAME" | grep -q "simulator"; then
  export DEV=false
  export RCT_NO_LAUNCH_PACKAGER=true
  export EXPO_USE_DEV_SERVER=0
fi

# Resolve entry file if not defined
if [ -z "$ENTRY_FILE" ]; then
  export ENTRY_FILE="$("$NODE_BINARY" -e "require('expo/scripts/resolveAppEntry')" "$PROJECT_ROOT" ios absolute | tail -n 1)"
fi

# Resolve Expo CLI path if not defined
if [ -z "$CLI_PATH" ]; then
  export CLI_PATH="$("$NODE_BINARY" --print "require.resolve('@expo/cli', { paths: [require.resolve('expo/package.json')] })")"
fi

# Default Expo bundling command for embedded builds
if [ -z "$BUNDLE_COMMAND" ]; then
  export BUNDLE_COMMAND="export:embed"
fi

# Allow Expo updates to modify SKIP_BUNDLING (but don't let it re-enable devtools on device)
if [ -f "$PODS_ROOT/../.xcode.env.updates" ]; then
  # shellcheck disable=SC1090
  . "$PODS_ROOT/../.xcode.env.updates"
fi

# Re-load local overrides if they exist (Expo template does this)
# BUT: we will force device-debug safety flags again afterward.
if [ -f "$PODS_ROOT/../.xcode.env.local" ]; then
  # shellcheck disable=SC1090
  . "$PODS_ROOT/../.xcode.env.local"
fi

# --- FINAL GUARD (must be last) ----------------------------------------------
# This is the critical part: ensure embedded device debug never tries devtools websocket.
if [ "\${CONFIGURATION#*Debug}" != "$CONFIGURATION" ] && ! echo "$PLATFORM_NAME" | grep -q "simulator"; then
  export DEV=false
  export RCT_NO_LAUNCH_PACKAGER=true
  export EXPO_USE_DEV_SERVER=0
fi

# (Optional) uncomment for debugging build logs:
# echo "CONFIGURATION=$CONFIGURATION PLATFORM_NAME=$PLATFORM_NAME DEV=$DEV SKIP_BUNDLING=$SKIP_BUNDLING EXPO_USE_DEV_SERVER=$EXPO_USE_DEV_SERVER"

# Run React Native Xcode bundling script
RN_XCODE_SCRIPT="$("$NODE_BINARY" --print "require('path').dirname(require.resolve('react-native/package.json')) + '/scripts/react-native-xcode.sh'")"
"$RN_XCODE_SCRIPT"
`;

const unquote = (value) => String(value || "").replace(/^"(.*)"$/, "$1");
const toPbxQuotedScript = (script) =>
  `"${script
    .replace(/\\/g, "\\\\")
    .replace(/\n/g, "\\n")
    .replace(/"/g, '\\"')}"`;

module.exports = function withDeviceDebugBundling(config) {
  config = withXcodeProject(config, (config) => {
    const xcodeProject = config.modResults;
    const buildConfigs = xcodeProject.pbxXCBuildConfigurationSection();

    for (const key of Object.keys(buildConfigs)) {
      const buildConfig = buildConfigs[key];
      if (typeof buildConfig !== "object" || !buildConfig.buildSettings) continue;
      buildConfig.buildSettings.ENABLE_USER_SCRIPT_SANDBOXING = "NO";
    }

    const shellPhases =
      xcodeProject.hash?.project?.objects?.PBXShellScriptBuildPhase || {};
    for (const [phaseId, phase] of Object.entries(shellPhases)) {
      if (phaseId.endsWith("_comment")) continue;
      if (!phase || typeof phase !== "object") continue;
      if (unquote(phase.name) !== BUNDLE_PHASE_NAME) continue;
      phase.shellScript = toPbxQuotedScript(
        BUNDLE_SCRIPT.endsWith("\n") ? BUNDLE_SCRIPT : `${BUNDLE_SCRIPT}\n`
      );
    }

    return config;
  });

  return withDangerousMod(config, [
    "ios",
    async (config) => {
      const iosRoot = config.modRequest.platformProjectRoot;
      const xcodeprojDir = fs
        .readdirSync(iosRoot)
        .find((name) => name.endsWith(".xcodeproj"));

      if (!xcodeprojDir) return config;

      const pbxprojPath = path.join(iosRoot, xcodeprojDir, "project.pbxproj");
      let pbxproj = fs.readFileSync(pbxprojPath, "utf8");

      // Fallback textual replacement, in case a config block already has YES.
      pbxproj = pbxproj.replaceAll(USER_SCRIPT_SANDBOXING_YES, USER_SCRIPT_SANDBOXING_NO);
      fs.writeFileSync(pbxprojPath, pbxproj);

      // Keep 3rd-party Pod warnings from drowning actionable app warnings.
      const podfilePath = path.join(iosRoot, "Podfile");
      if (fs.existsSync(podfilePath)) {
        let podfile = fs.readFileSync(podfilePath, "utf8");
        if (!podfile.includes(PODFILE_INHIBIT_WARNINGS_LINE)) {
          podfile = podfile.replace(
            PODFILE_PLATFORM_LINE,
            `${PODFILE_PLATFORM_LINE}${PODFILE_INHIBIT_WARNINGS_LINE}`
          );
          fs.writeFileSync(podfilePath, podfile);
        }
      }

      // Make "Run" default to Release on physical-device launches from Xcode.
      const xcodeprojRoot = path.join(iosRoot, xcodeprojDir);
      const walk = (dir) => {
        for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
          const fullPath = path.join(dir, entry.name);
          if (entry.isDirectory()) {
            walk(fullPath);
            continue;
          }
          if (!entry.name.endsWith(".xcscheme")) continue;
          let scheme = fs.readFileSync(fullPath, "utf8");
          if (scheme.includes(SCHEME_LAUNCH_DEBUG)) {
            scheme = scheme.replace(SCHEME_LAUNCH_DEBUG, SCHEME_LAUNCH_RELEASE);
            fs.writeFileSync(fullPath, scheme);
          }
        }
      };
      if (fs.existsSync(xcodeprojRoot)) {
        walk(xcodeprojRoot);
      }

      return config;
    },
  ]);
};
